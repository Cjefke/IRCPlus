 USŁUGI IRC
======================================================================

 Rejestracja pseudonimu:
     /msg NickServ HELP

 Zarządzanie kanałami:
     /msg ChanServ HELP

 Przydatne polecenia:

     /msg NickServ REGISTER
     /msg NickServ IDENTIFY
     /msg ChanServ REGISTER #kanałów

